// Concrete Component
public class ItemBasico extends ItemPedido {
    private double preco;

    public ItemBasico(String descricao, double preco) {
        this.descricao = descricao;
        this.preco = preco;
    }

    @Override
    public String getDescricao() {
        return descricao;
    }

    @Override
    public double getPreco() {
        return preco;
    }
}